package com.latihan;

public class Main {

    public static void main(String[] args) {

        // tipe data pada java --> integer, double, short, long, float, byte, boolean

        // integer
        System.out.println("==========Integer==========");
        int a = 1;
        System.out.println("tipe data a --> " + a);
        System.out.println("max tipe data --> " + Integer.MAX_VALUE);
        System.out.println("min tipe data --> " + Integer.MIN_VALUE);
        System.out.println("Ukuran tipe data (Byte) --> " + Integer.BYTES);
        System.out.println("Ukuran tipe data (Bit) --> " + Integer.SIZE);
        // double
        System.out.println("==========Double==========");
        double d = 1;
        System.out.println("tipe data d --> " + d);
        System.out.println("max tipe data --> " + Double.MAX_VALUE);
        System.out.println("min tipe data --> " + Double.MIN_VALUE);
        System.out.println("Ukuran tipe data (Byte) --> " + Double.BYTES);
        System.out.println("Ukuran tipe data (Bit) --> " + Double.SIZE);
        // short
        System.out.println("==========Short==========");
        short s = 1;
        System.out.println("tipe data s --> " + s);
        System.out.println("max tipe data --> " + Short.MAX_VALUE);
        System.out.println("min tipe data --> " + Short.MIN_VALUE);
        System.out.println("Ukuran tipe data (Byte) --> " + Short.BYTES);
        System.out.println("Ukuran tipe data (Bit) --> " + Short.SIZE);
        // Long
        System.out.println("==========Long==========");
        long l = 1;
        System.out.println("tipe data l --> " + l);
        System.out.println("max tipe data --> " + Long.MAX_VALUE);
        System.out.println("min tipe data --> " + Long.MIN_VALUE);
        System.out.println("Ukuran tipe data (Byte) --> " + Long.BYTES);
        System.out.println("Ukuran tipe data (Bit) --> " + Long.SIZE);
        // flaot
        System.out.println("==========float==========");
        float f = 1;
        System.out.println("tipe data f --> " + f);
        System.out.println("max tipe data --> " + Float.MAX_VALUE);
        System.out.println("min tipe data --> " + Float.MIN_VALUE);
        System.out.println("Ukuran tipe data (Byte) --> " + Float.BYTES);
        System.out.println("Ukuran tipe data (Bit) --> " + Float.SIZE);
        // Byte
        System.out.println("==========Byte==========");
        byte b = 2;
        System.out.println("tipe data b --> " + b);
        System.out.println("max tipe data --> " + Byte.MAX_VALUE);
        System.out.println("min tipe data --> " + Byte.MIN_VALUE);
        System.out.println("Ukuran tipe data (Byte) --> " + Byte.BYTES);
        System.out.println("Ukuran tipe data (Bit) --> " + Byte.SIZE);
        // Boolean
        System.out.println("==========Boolean==========");
        boolean bool = true;
        System.out.println("tipe data bool --> " + bool);
        // System.out.println("max tipe data --> " + Boolean.MAX_VALUE); --> boolean tidak mempunyai max karena terbesar 1
        // System.out.println("min tipe data --> " + Boolean.MIN_VALUE); --> boolean tidak mempunyai min karena terkecil 0
        // System.out.println("Ukuran tipe data (Byte) --> " + Boolean.BYTES);
        // System.out.println("Ukuran tipe data (Bit) --> " + Boolean.SIZE); --> boolean tidak mempunyai size karena hanya 1 dan 0
    }
}
